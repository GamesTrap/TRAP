#include "TRAPPCH.h"
#include "TimeStep.h"

//-------------------------------------------------------------------------------------------------------------------//

TRAP::Utils::TimeStep::TimeStep(const float time)
	: m_time(time)
{
}

//-------------------------------------------------------------------------------------------------------------------//

TRAP::Utils::TimeStep::operator float() const
{
	return m_time;
}

//-------------------------------------------------------------------------------------------------------------------//

float TRAP::Utils::TimeStep::GetSeconds() const
{
	return m_time;
}

//-------------------------------------------------------------------------------------------------------------------//

float TRAP::Utils::TimeStep::GetMilliseconds() const
{
	return m_time * 1000.0f;
}