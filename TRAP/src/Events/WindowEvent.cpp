[[nodiscard]] std::string TRAP::Events::WindowDropEvent::ToString() const
{
	std::string out = fmt::format("WindowDropEvent: ");

	if(!m_paths.empty())
		out += fmt::format("Path(s): \"{}\" ", fmt::join(m_paths, "\", \""));

	out += fmt::format("(\"{}\")", m_window.get().GetTitle());

	return out;
}

